/**
 * Class representing a keyboard state with boolean flags for key states.
 */
class Keyboard {
    /**
     * @property {boolean} LEFT - Flag indicating the state of the left arrow key.
     * @property {boolean} RIGHT - Flag indicating the state of the right arrow key.
     * @property {boolean} UP - Flag indicating the state of the up arrow key.
     * @property {boolean} DOWN - Flag indicating the state of the down arrow key.
     * @property {boolean} SPACE - Flag indicating the state of the spacebar key.
     */
    LEFT = false;
    RIGHT = false;
    UP = false;
    DOWN = false;
    SPACE = false;
}